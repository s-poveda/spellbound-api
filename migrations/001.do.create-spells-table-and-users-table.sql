CREATE TABLE spells (
	id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
	title TEXT DEFAULT 'Untitled spell',
	description TEXT,
	date_created TIMESTAMPTZ DEFAULT now() NOT NULL
);
CREATE TABLE users (
	id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
	username VARCHAR(15) NOT NULL,
	password TEXT CONSTRAINT pwdlen CHECK (CHAR_LENGTH(password) >= 8)
);
